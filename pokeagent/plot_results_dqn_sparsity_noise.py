"""
plot_results_dqn.py
Reads CSVs generated by run_experiments_dqn.sh and plots evaluation metrics.

Generates:
  - exp1_plot.png:          Learning curves across all modes & sparsity levels
  - exp2_plot.png:          Robustness line comparison (existing)
  - exp2_robustness_bar.png: Robustness bar chart (NEW)
  - exp3_plot.png:          Guidance frequency comparison
  - exp1_sparsity_comparison.png: Sparsity comparison (NEW)
"""
import os
import glob
import pandas as pd
import numpy as np
import matplotlib
matplotlib.use('Agg')
import matplotlib.pyplot as plt

RESULTS_DIR = "results"
WINDOW = 50  # rolling average window

def safe_read(path):
  """Read CSV, handle both old (4-col) and new (7-col) formats."""
  if not os.path.exists(path):
    return None
  df = pd.read_csv(path)
  if 'steps' not in df.columns:
    df['steps'] = 200  # default for MountainCar truncation
  if 'success' not in df.columns:
    df['success'] = 0
  return df


# ──────────────────────────────────────────────────
#  Experiment 1: Learning Curves (all modes × sparsity)
# ──────────────────────────────────────────────────
def plot_experiment_1():
  fig, ax = plt.subplots(figsize=(12, 6))
  for file in sorted(glob.glob(f"{RESULTS_DIR}/exp1_*.csv")):
    label = os.path.basename(file).replace("exp1_", "").replace(".csv", "")
    df = safe_read(file)
    if df is None:
      continue
    smoothed = df['total_reward'].rolling(window=WINDOW, min_periods=1).mean()
    ax.plot(df['episode'], smoothed, label=label, linewidth=1.5)

  ax.set_title("Experiment 1: Sample Efficiency across Sparsity Levels", fontsize=14)
  ax.set_xlabel("Episode")
  ax.set_ylabel(f"Rolling Avg Env Reward (window={WINDOW})")
  ax.legend(fontsize=9, ncol=2)
  ax.grid(True, alpha=0.3)
  fig.tight_layout()
  fig.savefig(f"{RESULTS_DIR}/exp1_plot.png", dpi=150)
  plt.close(fig)
  print("Saved results/exp1_plot.png")


